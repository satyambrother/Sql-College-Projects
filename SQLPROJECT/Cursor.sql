THERE ARE SIX METHOD USE FOR EXPLICIT MANNER:

NEXT
FIRST
LAST
PRIOR
ABSOLUTE N
RELATIVE N

1: WITHOUT CURSOR VARIABLE

DECLARE C1 CURSOR SCROLL FOR SELECT*FROM EMP1
OPEN C1
FETCH LAST FROM C1
FETCH PRIOR FROM C1
FETCH ABSOLUTE 4 FROM C1
FETCH RELATIVE -2 FROM C1

FETCH FIRST FROM C1
FETCH NEXT FROM C1
CLOSE C1
DEALLOCATE C1

2: WITH CURSOR VARIABLE

DECLARE C1 CURSOR SCROLL FOR SELECT EID,NAME FROM EMP1

DECLARE @EID INT, @NAME VARCHAR(20) 
OPEN C1

FETCH LAST FROM C1 INTO @EID,@NAME
PRINT'THE EMPLOYEE'+'@NAME+'+'ID IS:-'+CAST(@EID AS VARCHAR)


FETCH PRIOR FROM C1 INTO @EID,@NAME
PRINT'THE EMPLOYEE'+'@NAME+'+'ID IS:-'+CAST(@EID AS VARCHAR)


FETCH ABSOLUTE 4 FROM C1 INTO @EID,@NAME
PRINT'THE EMPLOYEE'+'@NAME+'+'ID IS:-'+CAST(@EID AS VARCHAR)


FETCH RELATIVE -2 FROM C1 INTO @EID,@NAME
PRINT'THE EMPLOYEE'+'@NAME+'+'ID IS:-'+CAST(@EID AS VARCHAR)


FETCH FIRST FROM C1 INTO @EID,@NAME
PRINT'THE EMPLOYEE'+'@NAME+'+'ID IS:-'+CAST(@EID AS VARCHAR)


FETCH NEXT FROM C1 INTO @EID,@NAME
PRINT'THE EMPLOYEE'+'@NAME+'+'ID IS:-'+CAST(@EID AS VARCHAR)

CLOSE C1
DEALLOCATE C1
	                   
