3----ROLLUP CLAUSE ROLLUP IS FINDING SUB AND GRAND TOTAL BASED ON A SINGLE COLUMN

4-----CUBE CLAUSE IS FINDING SUB AND GRAND TOTAL BASED ON MULTIPLE COLUMN 

THESE UPPER CLAUSE IS USED ALONG WITHY GROUP BY

