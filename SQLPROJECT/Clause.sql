2----HAVING CLAUSE =====> THIS CLAUSE USED ON AFTER GROUPING THE DATA IT IS MAINLY USED TO FILTERING THE GROUPED DATA 

SELECT JOB,COUNT(*) FROM EMP GROUP BY JOB    

WHICH JOB CONTAIN NUMBER OF EMPLOYEE LESS THAN 4

SELECT JOB,COUNT(*) FROM EMP GROUP BY JOB HAVING COUNT(*)<4

